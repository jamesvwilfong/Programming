public class Letters {
   
   public static void main(String[] args) {
  
      final String LETTER_M = "    *     *\n   * *   * *"
               + "\n  *   * *   *\n *     *     *\n*             *";
      
      final String LETTER_A = "       *      \n      * *"
               + "\n     *   *\n    *     *\n   *********"
               + "\n  *         *\n *           *\n*             *";
      
      final String LETTER_I = "***************\n       *"
               + "\n       *\n       *\n       *\n       *\n"
               + "       *\n***************";
      
      final String LETTER_L = "*              \n*              \n"
               + "*              \n*              \n*              \n"
               + "*              \n*              \n***************";
      
      
      // Dislays the word MAIL
      System.out.println(LETTER_M);
      System.out.println();
      System.out.println(LETTER_A);
      System.out.println();
      System.out.println(LETTER_I);
      System.out.println();
      System.out.println(LETTER_L);
   }
}
